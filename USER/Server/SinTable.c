uint16_t  getSinValue(uint16_t i)
{
	return sinTable[i&(SINMAX-1)];
}
uint16_t  getSinValueFl(float angel)
{
	return getSinValue(angel/3.14159f*(SINMAX>>1));
}
uint16_t  getCosValue(uint16_t i)
{
	return sinTable[SINMAX - (i&SINMAX-1)-1];
}
uint16_t  getCosValueFl(float angel)
{
	return getCosValue(angel/3.14159f*(SINMAX>>1));
}

